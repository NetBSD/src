/*
 * Set up the real-time and statistics clocks.  Leave stathz 0 only if
 * no alternative timer is available.
 */
void
cpu_initclocks(void)
{

	switch (mach_type) {
#if defined(MIPS1)
	case MACH_SGI_IP6 | MACH_SGI_IP10:
	case MACH_SGI_IP12:
		/* int(4) will take care of our clocks */
		/* enable hardware interrupts including hardclock(9) */
		spl0();
		break;
#endif /* MIPS1 */
#if defined(MIPS3)
	case MACH_SGI_IP20:
	case MACH_SGI_IP22:
	case MACH_SGI_IP30:
	case MACH_SGI_IP32:
		mips3_initclocks();
		break;
#endif /* MIPS3 */
	default:
		panic("cpu_initclocks(): unknown mach_type IP%d", mach_type);
		break;
	}
}

#if defined(MIPS3)
void
mips3_clock_intr(vaddr_t pc, uint32_t status, uint32_t pending)
{
	struct clockframe cf;

	cf.pc = pc;
	cf.sr = status;
	cf.intr = (curcpu()->ci_idepth > 1);
	mips3_clockintr(&cf);
}

#endif /* MIPS3 */
