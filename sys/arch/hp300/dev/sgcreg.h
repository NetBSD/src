/*
 * Register definitions for the SGC bus.
 */

/*
 * SGC ranges from slots 0-3 at physical addresses given by:
 *	0x20000000 + slot * 0x04000000
 */

#define	SGC_BASE		0x20000000
#define	SGC_END			0x30000000
#define	SGC_DEVSIZE		0x04000000

#define	SGC_NSLOTS		4
