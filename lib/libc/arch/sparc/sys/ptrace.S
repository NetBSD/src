#include "SYS.h"

ENTRY(ptrace)
#ifdef _REENTRANT
	save	%sp, -CCFSZ, %sp
	call	_C_LABEL(__errno)
	 nop
	st	%g0, [%o0]
	restore
#else
#ifdef __PIC__
	PIC_PROLOGUE(%g1,%g2)
	set	_C_LABEL(errno), %g2
	ld	[%g1 + %g2], %g1
	st	%g0, [%g1]
#else
	sethi	%hi(_C_LABEL(errno)), %g1
	st	%g0, [%g1 + %lo(_C_LABEL(errno))]
#endif
#endif /* !_REENTRANT */
	mov	SYS_ptrace, %g1
	t	ST_SYSCALL
	bcc	1f
	 nop
	ERROR()
1:
	retl
	 nop
