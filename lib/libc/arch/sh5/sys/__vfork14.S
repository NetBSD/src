/*
 * pid = vfork();
 *
 * r3 == 0 in parent process, r3 == 1 in child process.
 * r2 == pid of child in parent, r2 == pid of parent in child.
 */

ENTRY(__vfork14)
	SYSTRAP(__vfork14)
	pta/u	1f, tr0
	bne/u	r0, r63, tr0
	ptabs/l	r18, tr0
	cmvne	r3, r63, r2
	blink	tr0, r63

1:	PIC_PROLOGUE
	PIC_PTAL(_C_LABEL(__cerror), r0, tr0)
	blink	tr0, r63
