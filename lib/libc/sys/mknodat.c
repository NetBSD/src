#include <sys/syscall.h>
#include <sys/stat.h>

int __mknodat(int, const char *, mode_t, int, dev_t);

int
mknodat(int fd, const char *path, mode_t mode, dev_t dev)
{
	return __mknodat(fd, path, mode, 0, dev);
}
